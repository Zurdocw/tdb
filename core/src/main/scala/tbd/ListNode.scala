package tbd

import tbd.mod.Mod

class ListNode[T](aValue: Mod[T], aNext: Mod[ListNode[T]]) {
  val value = aValue
  val next = aNext

  override def toString: String = {
    def toString(lst: ListNode[T]):String = {
      val nextRead = lst.next.read()
      val next =
	if (nextRead != null)
	  ", " + toString(nextRead)
	else
	  ")"

      lst.value.read() + next
    }

    "(" + toString(this)
  }
}
